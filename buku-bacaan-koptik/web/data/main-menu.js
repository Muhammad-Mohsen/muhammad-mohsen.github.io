	function setURI(base, menu) {
		menu.forEach(m => {
			m.uri = base + '/' + toURI(m.text.toLowerCase()?.replace(/ /g, '-'));
			if (m.menu?.length) setURI(m.uri, m.menu);
		});
	}

	function toURI(text) {
		return text.toLowerCase()?.replace(/ /g, '-')?.replace(/'/g, '');
	}

	function getEntry(uri) {
		const parts = uri.split('/');
		parts.shift();

		let entry = { menu: DATA }; // to keep the symmetry
		parts.forEach(p => entry = entry.menu.find(m => toURI(m.text) == p));

		return entry;
	}

	function getEntryByPath(menu, path) {
		for (let m of menu) {
			if (m.path == path) return m;
			if (m.menu) {
				const mm = getEntryByPath(m.menu, path);
				if (mm) return mm;
			}
		}
	}

	function getParent(uri) {
		const parts = uri.split('/');
		parts.shift();
		parts.pop();

		return getEntry(`/${parts.join('/')}`);
	}

	return {
		DATA: DATA,

		setURI: setURI,
		toURI: toURI,
		getEntry: getEntry,
		getEntryByPath: getEntryByPath,
		getParent: getParent,
	}

